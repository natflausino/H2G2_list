void	ft_sort_int_tab(int *tab, int size)
{
	int i;
	int j;
	int tab_a;

	i = 0;
	while (i < size)
	{
		j = 0;
		while (j < size)
		{
			if (tab[i] < tab[j])
			{
				tab_a = tab[j];
				tab[j] = tab[i];
				tab[i] = tab_a;
			}
			j++;
		}
		i++;
	}
}
